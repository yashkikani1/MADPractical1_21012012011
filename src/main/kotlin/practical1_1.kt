fun main(){
    var int:Int=100
    val float:Float=200.95F
    val char:Char='a'
    val string:String="Hello World"
    val boolean:Boolean=true
    val double:Double=45.56
    val long:Long=12345
    val short:Short=-7
    val byte:Byte=127
    println("Integer Value: $int")
    println("Float Value: $float")
    println("Character Value: $char")
    println("String Value: $string")
    println("Boolean Value: $boolean")
    println("Double Value: $double")
    println("Long Value: $long")
    println("Short Value: $short")
    println("Byte Value: $byte")
}